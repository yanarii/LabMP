# Запрашиваем у пользователя ввод предложения
sentence = input("Введите предложение: ")

# Разбиваем введённую строку на список слов по пробелам
# Метод split(" ") возвращает список слов
words = sentence.split(" ")

# Считаем количество слов с помощью функции len()
# len(words) возвращает длину списка, то есть количество слов
print("Количество слов в предложении:", len(words))
